package projetoRPG;

public class Inimigos {
	private String nomeInimigo;
	private double vidaInimigo;
	private double atkInimigo;
	private double armaduraInimigo;
	
	public String getNomeInimigo() {
		return nomeInimigo;
	}
	public void setNomeInimigo(String nomeInimigo) {
		this.nomeInimigo = nomeInimigo;
	}
	public Double getVidaInimigo() {
		return vidaInimigo;
	}
	public void setVidaInimigo(double vidaInimigo) {
		this.vidaInimigo = vidaInimigo;
	}
	
	public Double getAtkInimigo() {
		return atkInimigo;
	}
	public void setAtkInimigo(double atkInimigo) {
		this.atkInimigo = atkInimigo;
	}
	
	public Double getArmaduraInimigo() {
		return armaduraInimigo;
	}
	public void setArmaduraInimigo(double armaduraInimigo) {
		this.armaduraInimigo = armaduraInimigo;
	}
	
}
